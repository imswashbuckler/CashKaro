package AProgramsPorblems;

public class _QueenLocation_Practo {
	
	int[][] abc= new int[4][4]; 
	int p=abc.length;
	int q=abc[0].length;
	int count=0;
	public void QueenLocation( int k,int l)
	{
		
		for(int i=k;i<k;i++)
		{
			for(int j=0;j<q;j++)
			{	
				count=count+1;
			}
		}
		
		for(int i=0;i<p;i++)
		{
			for(int j=l;j<=l;j++)
			{	
				count=count+1;
			}
		}
		
		for(int i=0;i<p;i++)
		{
			for(int j=l;j<=l;j++)
			{	
				count=count+1;
			}
		}
		
		
		
		
	}
	

}
